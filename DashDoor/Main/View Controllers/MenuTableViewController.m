    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    self.menuOptionArray = @[@"Restaurants", @"Account", @"Support", @"Become a Driver", @"Logout"];
    self.segueArray = @[@"firstRow", @"secondRow", @"thirdRow", @"fourthRow", @"fifthRow"];
}

