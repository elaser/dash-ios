typedef enum {
    MENU_RESTAURANTS,
    MENU_ACCOUNT,
    MENU_SUPPORT,
    MENU_DRIVER,
    MENU_LOGOUT,
    NUM_OF_MENU_OPTIONS
}MENU_OPTIONS;

@interface MenuTableViewController ()

@property NSArray *menuOptionArray;
@property NSArray *segueArray;

@end

@implementation MenuTableViewController

